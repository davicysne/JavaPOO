package LISTA2.Q01_LISTA2;

public class contador {
    public int valor;

    public int getValor(){
        return valor;
    }
    public void setValor(){
        this.valor = valor;
    }

    public void zerar(){
        this.valor = 0;
    }

    public void incrementar(){
        this.valor = valor + 1;
    }
}
